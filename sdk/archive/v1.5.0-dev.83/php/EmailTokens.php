    }

    public function deleteUserEmailTokens(int $userId): mixed {
        $this->ctx->requireTokens();
        $path = "/api/v1/admin/email_tokens/by_user/" . rawurlencode((string)$userId);
        return $this->ctx->client->request($path, 'DELETE', $this->ctx->accessToken, $this->ctx->refreshToken)->data;
    }

    private static function parseHeaders(string $raw): array {
        $headers = [];
        foreach (explode("\r\n", $raw) as $line) {
            $parts = explode(':', $line, 2);
            if (count($parts) === 2) {
                $headers[strtolower(trim($parts[0]))] = trim($parts[1]);
            }
        }
        return $headers;
    }
}
